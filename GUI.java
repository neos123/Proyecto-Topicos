    private void iniciarLista() {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection cnx = DriverManager.getConnection("jdbc:mysql://localhost:3306/segumientoprogramatico2", "root", "sql123");
            String query = "SELECT grupo FROM segumientoprogramatico2.Materia";
            Statement st = cnx.createStatement();
            ResultSet rs = st.executeQuery(query);

            while (rs.next()) {
                ListaGrupos.addItem(rs.getString(1));
            }
            jPanel2.add(ListaGrupos);
            cnx.close();
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        } catch (ClassNotFoundException ex) {
            System.out.println(ex.getMessage());
        }
    }

